package Chapter6;

public class Printer {

    public static void printer(int returnValue) {

        System.out.println(returnValue);

    }


    public static void printer(boolean returnValue) {

        System.out.println(returnValue);

    }

    public static void printer(double returnValue) {

        System.out.println(returnValue);

    }

    public static void printer(String returnValue) {

        System.out.println(returnValue);

    }

}
